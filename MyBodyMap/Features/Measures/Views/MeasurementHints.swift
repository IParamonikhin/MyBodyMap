import Foundation

struct MeasurementHints {
    static let hints: [String: String] = [
        "height":
            """
            • Встаньте у стены, пятки, ягодицы и затылок касаются стены.
            • Голова прямо, взгляд вперёд.
            • Используйте книгу или угольник — прижмите к макушке, отметьте точку на стене.
            • Измерьте расстояние от пола до отметки.
            """,
        "neck":
            """
            • Встаньте прямо, смотрите вперёд.
            • Ленту оберните вокруг шеи чуть ниже адамова яблока (или самой тонкой части).
            • Не затягивайте, лента должна плотно прилегать.
            """,
        "shoulders":
            """
            • Встаньте ровно, расслабьте руки вдоль тела.
            • Лента — по самой широкой части плеч (дельты), окружая оба плеча и лопатки.
            • Лучше делать с чьей-то помощью — лента строго горизонтально.
            """,
        "chest":
            """
            • Встаньте прямо, руки расслаблены.
            • Лента — по выступающим точкам грудных мышц (у мужчин — по соскам, у женщин — по центру груди).
            • Дышите спокойно, измеряйте на выдохе.
            """,
        "biceps":
            """
            • Руку согните под углом 90° (локоть в сторону).
            • Найдите самую широкую часть бицепса.
            • Ленту оберните перпендикулярно руке, не натягивая.
            """,
        "waist":
            """
            • Встаньте ровно, расслабьтесь.
            • Лента проходит горизонтально по самой узкой части туловища (обычно чуть выше пупка).
            • Не втягивайте живот!
            """,
        "forearm":
            """
            • Руку расслабьте, опустите вниз.
            • Лента — по самой широкой части предплечья (между локтем и запястьем).
            """,
        "stomach":
            """
            • Измеряйте по самой выступающей части живота (обычно по пупку или чуть ниже).
            • Встаньте прямо, не втягивайте живот, дышите спокойно.
            """,
        "buttocks":
            """
            • Встаньте ровно, ноги вместе.
            • Лента — по самой выступающей точке ягодиц.
            • Лента параллельна полу.
            """,
        "thigh":
            """
            • Встаньте прямо, перенесите вес на левую ногу, правую расслабьте.
            • Лента — по самой широкой части бедра (обычно 2–5 см ниже паха).
            """,
        "calf":
            """
            • Встаньте, расслабьтесь.
            • Лента — по самой широкой части икры.
            """,
        "weight":
            """
            • Используйте напольные весы на ровной поверхности.
            • Взвешивайтесь утром, после туалета, без одежды или в лёгкой одежде.
            """
    ]
    
    static func hint(for field: String) -> String? {
        hints[field]
    }
}
